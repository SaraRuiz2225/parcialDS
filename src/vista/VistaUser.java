    public void limpiar() {
        txtNombre.setText("");
        txtApellido.setText("");
        txtCedula.setEnabled(true);
        txtCorreo.setText("");
        txtContrase単a.setText("");
    }
    
    public void llenarTabla() {
        DefaultTableModel model = new DefaultTableModel();
        List<Usuario> lista = controlador.obtenerLista();
        model.setColumnIdentifiers(new Object[]{"nombre", "apellido", "cedula", "correo", "contrase単a"});
        tlbUsuario.setModel(model);
        for (int i = 0; i < lista.size(); i++) {
            model.addRow(new Object[]{
                lista.get(i).getNombre(),
                lista.get(i).getApellido(),
                lista.get(i).getCedula(),
                lista.get(i).getCorreo(),
                lista.get(i).getContrase単a()
            });

        }
    }

